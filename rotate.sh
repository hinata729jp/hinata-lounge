#!/usr/bin/env bash
#
# rotate.sh
# ------------------------------------------------------------
# 「LINE登録者限定」ページの公開フォルダ名を差し替えるスクリプトです。
#
# 目的:
#   退会したかたが、以前に配ったURL（旧フォルダ名）へアクセスできないよう、
#   コンテンツフォルダの名前を新しいランダムな文字列へ定期的に付け替えます。
#
# 動作:
#   1. 現在のコンテンツフォルダ（index.html が入っているフォルダ）を自動で特定
#   2. 新しいランダムな英数字フォルダ名を生成
#   3. 中身をまるごと新フォルダへコピー
#   4. 古いフォルダを削除（＝旧URLを無効化）
#   5. 新しい公開URL（フォルダ部分）を表示
#
# 実行後は、表示される git コマンドで GitHub へ反映してください。
# ------------------------------------------------------------

set -euo pipefail

# --- このスクリプトが置かれているディレクトリ（＝リポジトリ直下）へ移動 ---
cd "$(dirname "$0")"

# --- 1. 現在のコンテンツフォルダを特定する ---------------------------------
# リポジトリ直下にある「index.html を含むフォルダ」を探します。
# .git などの隠しフォルダは除外し、対象は必ず1つだけである前提です。
CURRENT_DIR=""
for d in */ ; do
  d="${d%/}"                       # 末尾のスラッシュを除去
  case "$d" in
    .*|node_modules) continue ;;   # 隠しフォルダ・依存フォルダはスキップ
  esac
  if [ -f "$d/index.html" ]; then
    if [ -n "$CURRENT_DIR" ]; then
      echo "エラー: コンテンツフォルダが複数見つかりました（$CURRENT_DIR と $d）。" >&2
      echo "        どちらが正しいか手動で確認してください。中断します。" >&2
      exit 1
    fi
    CURRENT_DIR="$d"
  fi
done

if [ -z "$CURRENT_DIR" ]; then
  echo "エラー: index.html を含むコンテンツフォルダが見つかりません。中断します。" >&2
  exit 1
fi

# --- 2. 新しいランダムなフォルダ名を生成する（英小文字＋数字 8文字） ---------
NEW_DIR="$(LC_ALL=C tr -dc 'a-z0-9' < /dev/urandom | head -c 8)"

# 生成結果が空でないことを確認（保険）
if [ -z "$NEW_DIR" ]; then
  echo "エラー: 新しいフォルダ名の生成に失敗しました。中断します。" >&2
  exit 1
fi

# 万一すでに同名が存在したら、上書き事故を防ぐため中断
if [ -e "$NEW_DIR" ]; then
  echo "エラー: 生成した名前「$NEW_DIR」がすでに存在します。もう一度実行してください。" >&2
  exit 1
fi

# --- 3. 中身をまるごと新フォルダへコピーする -------------------------------
mkdir "$NEW_DIR"
cp -R "$CURRENT_DIR"/. "$NEW_DIR"/     # 隠しファイルも含めて全部コピー

# --- 4. 古いフォルダを削除する（＝旧URLを無効化） --------------------------
# 事故防止のため、削除前に以下をすべて確認します:
#   ・変数が空でない  ・実在するディレクトリである  ・新フォルダと別物である
if [ -n "$CURRENT_DIR" ] && [ -d "$CURRENT_DIR" ] && [ "$CURRENT_DIR" != "$NEW_DIR" ]; then
  rm -rf -- "$CURRENT_DIR"
else
  echo "エラー: 古いフォルダの削除条件を満たしませんでした。安全のため中断します。" >&2
  exit 1
fi

# --- 5. 新しい公開URL（フォルダ部分のパス）を表示する ----------------------
echo "------------------------------------------------------------"
echo "フォルダ名を差し替えました。"
echo "  旧: $CURRENT_DIR/  (削除済み)"
echo "  新: $NEW_DIR/"
echo ""
echo "新しい公開パス: /$NEW_DIR/"
echo "  例) https://<ユーザー名>.github.io/<リポジトリ名>/$NEW_DIR/"
echo "------------------------------------------------------------"
echo ""
echo "GitHub へ反映するには、続けて次を実行してください:"
echo "  git add -A"
echo "  git commit -m \"rotate: $CURRENT_DIR -> $NEW_DIR\""
echo "  git push"
